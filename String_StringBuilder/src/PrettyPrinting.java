public class PrettyPrinting {
    public static void main(String[] args) {

        float a = 453.27656f;
        //Round of using Printf
        System.out.printf("Formatted number is %.2f",a);

        System.out.println(" ");
        System.out.printf("Pretty Printing %.2f",Math.PI);

        System.out.println(" ");
        System.out.printf("Hello my name is %s & i am a %s","Soham","Web Developer");
    }
}
